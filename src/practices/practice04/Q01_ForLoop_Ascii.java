package practices.practice04;

public class Q01_ForLoop_Ascii {

/*
    Print on the console numbers from 0 to 100.
    But do not use any number in your code!
*/

    public static void main(String[] args) {

        for (int i = 'e'-'e'; i < 'e'; i++) {

            System.out.print(i+" ");
        }


    }
}
